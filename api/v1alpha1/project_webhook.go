// ValidateCreate implements webhook.Validator so a webhook will be registered for the type
func (v *ProjectCustomValidator) ValidateCreate(ctx context.Context, obj runtime.Object) (admission.Warnings, error) {
	project, ok := obj.(*Project)
	if !ok {
		return nil, fmt.Errorf("expected a Project object but got %T", obj)
	}
	projectlog.Info("validate create", "name", project.Name)

	if utils.IsEmpty(project.Spec.Region) {
		return nil, errors.New(".spec.region must be specified")
	}

	return nil, nil
}

// ValidateUpdate implements webhook.Validator so a webhook will be registered for the type
func (v *ProjectCustomValidator) ValidateUpdate(ctx context.Context, oldObj, newObj runtime.Object) (admission.Warnings, error) {
	oldProject, ok := oldObj.(*Project)
	if !ok {
		return nil, fmt.Errorf("expected a Project object but got %T", oldObj)
	}
	newProject, ok := newObj.(*Project)
	if !ok {
		return nil, fmt.Errorf("expected a Project object but got %T", newObj)
	}
	projectlog.Info("validate update", "name", oldProject.Name)

	if oldProject.Spec.Region != newProject.Spec.Region {
		return nil, errors.New("region field is immutable")
	}

	return nil, nil
}

